package com.hmdm.rest.json;

import com.fasterxml.jackson.annotation.JsonIgnoreProperties;
import io.swagger.annotations.ApiModel;
import io.swagger.annotations.ApiModelProperty;

import java.io.Serializable;

/**
 * Request to register an existing server file (in work/files) as an MDM file
 * so it can be used in configurations without uploading again.
 */
@ApiModel(description = "Register a file already on the server")
@JsonIgnoreProperties(ignoreUnknown = true)
public class RegisterServerFileRequest implements Serializable {

    private static final long serialVersionUID = 1L;

    @ApiModelProperty("Relative path of the file in the files directory (e.g. Waze.xapk)")
    private String filePath;

    @ApiModelProperty("Optional description for the file")
    private String description;

    public String getFilePath() {
        return filePath;
    }

    public void setFilePath(String filePath) {
        this.filePath = filePath;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }
}
